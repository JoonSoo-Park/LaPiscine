#include <stdio.h>

void	ft_div_mod(int a, int b, int *div, int *mod)
{
	*div = a / b;
	*mod = a % b;
}

int		main(void)
{
	int div;
	int mod;

	ft_div_mod(9, 4, &div, &mod);
	printf("%d, %d\n", div, mod);
}
